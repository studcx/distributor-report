def format_rate_display(rate_val):
    """將折扣率格式化為顯示字串（如 x0.8, x0.5 等）"""
    if rate_val is None:
        return "x0"
    rv = float(rate_val)
    s = f"{rv:.3f}".rstrip("0")
    if "." not in s:
        s += ".0"
    return "x" + s


def load_distributor_extras():
    """從 JSON 檔案載入額外欄位資料（J-W 欄的套裝/雜項資訊）"""
    extras_file = os.path.join(BASE_DIR, "extras_from_04.json")
    if not os.path.exists(extras_file):
        return {}
    with open(extras_file, "r", encoding="utf-8") as f:
        data = json.load(f)
    result = {}
    for sname, extras in data.items():
        result[sname] = {}
        for k, items in extras.items():
            vtype = items[0] if len(items) > 0 else "t"
            vval = items[1] if len(items) > 1 else ""
            result[sname][k] = (vtype, vval)
    return result
